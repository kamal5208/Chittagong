package practice;

public class Variables {
    int salary= 1000;
    public void intVariables(){
        System.out.println("my salary is: " + salary);
    }
    public void floatVariables(){
        float price = 45.3f;
        System.out.println("price is: " + price);

    }
    public void doubleVariables(){
        float price = 45.55f;
        System.out.println("price is: " + price);
    }
    public void booleanVariables(){
        boolean red = true;
        boolean green = false;
        System.out.println("Red is: " + red + " Green is :" + green);
    }
    public void characterVariables(){
        char Cow = 'C';
        System.out.println("Cow is :"  + Cow);
    }
    public void stringVariables(){
        String name = "Kamal";
        String city = "New York";
        System.out.println("Name is: "  + name + " City is: "  + city);
    }
}
